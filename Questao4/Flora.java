package Questao4;

public class Flora {
	protected String tipoPlanta;
	protected int altura;
	
	
	public String getTipoPlanta() {
		return tipoPlanta;
	}
	public void setTipoPlanta(String tipoPlanta) {
		this.tipoPlanta = tipoPlanta;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}
	
	
}
